              兰州牛肉拉面技术，堪称西北一绝。\n拉面制作在中国流传已久，久负盛名，天下第一之技法。\n兰州牛肉面是技术含量较高的品种。\n\n折叠制面\n兰州牛肉面的制作的五大步骤无论从选料、和面、饧面，还是溜条和拉面，都巧妙地运用了所含成分的物理性能，即面筋蛋白质的延伸性和弹性。\n\n选面\n一般要选择新鲜的高筋面粉，兰州有牛肉面专用粉。\n\n和面\n和面是拉面制作的基础，是关键。\n首先应注意的是水的温度，一般要求冬天用温水，其它季节则用凉水。\n通过和面时用水温度的不同，使和好的面团温度始终保持在30℃，此时面粉中的蛋白质吸水性高，可以达到100%，面筋的生成率也高，质量好，即延伸性和弹性好，最适宜抻拉。\n其次，和面时还要放入适量的水和灰，提高面团中面筋的生成率和质量。\n讲究\"三遍水，三遍灰，九九八十一遍揉\"。\n其中的灰，是用戈壁滩所产的蓬草烧制出来的碱性物质，俗称蓬灰，加进面里，不仅使面有了一种特殊的香味，而且拉出来的面条爽滑透黄、筋道有劲。\n近年来已用专用的和面剂代替，和面技巧仍是最关键。\n\n饧面\n即将和好的面团放置一段时间(一般冬天不能低于30分钟，夏天稍短些)，其目的是促进面筋的生成，使没有充分吸收水分的蛋白质有充分的吸水时间，以提高面筋的生成和质量。\n\n溜面\n由膀圆力大的小伙子先将大团软面反复捣、揉、抻、摔后，将面团放在面板上，用两手握住条的两端，抬起在案板上用力摔打。\n条拉长后，两端对折，继续握住两端摔打，如此反复，其目的是调整面团内面筋蛋白质的排列顺序，使杂乱无章的蛋白质分子排列成一条长链，业内称其为顺筋。\n然搓成长条，揪成20毫米粗、筷子长的一条条面节，或搓成圆条。\n\n拉面\n将溜好的面条放在案板上，撒上清油(以防止面条粘连)，然后随食客的爱好，拉出大小粗细不同的面条。\n拉面是一手绝活，手握两端，两臂均匀用力加速向外抻拉，然后两头对折，两头同时放在一只手的指缝内(一般用左手)，另一只手的中指朝下勾住另一端，手心上翻，使面条形成绞索状，同时两手往两边抻拉。\n面条拉长后，再把右手勾住的一端套在左手指上，右手继续勾住另一端抻拉。\n抻拉时速度要快，用力要均匀，如此反复，每次对折称为一扣。\n一般二细均为7扣，细的则为9扣，毛细面可以达11扣，条细如丝，且不断裂，真可谓中国烹饪之精华。\n\n折叠熬汤\n兰州牛肉面的优劣除了拉面外，最重要的技含量在于淳香的汤，可以说汤是兰州牛肉面的灵魂。\n牛肉汤主要是几十种香料与牛肉原汤熬制而成。\n\n折叠汤的制法\n用牛肉、肥土鸡、牛肉为主料，采用三十多种调料和中药调配熬制的老汤，经沸煮、微煮，使主料鲜味溶于汤中，中间要经过两次\"清俏\"，成品清澈见底，味道极其鲜美，是牛肉面里最入味儿的汤料。\n\n熬汤时选用甘南草原上出产的肥嫩牦牛肉，加牛脊髓和腿骨(俗称棒子骨)，牛肝，有的还添入鸡汤，再按比例加入花椒、草果、桂子、姜皮等香料在特大罐形铁锅内再加入本地特产的绿萝卜片熬成即可。\n\n食材准备\n主料:肉牦牛。\n\n配料:牛骨头、牛肝、肥土鸡、绿萝卜、清油、葱花、食盐、香莱、蒜苗、辣子油酌量。\n\n调料:\"马家大爷牛肉面\"的调料配方主要有三种，最常用的是\"浓香型\"的。\n\n制作步骤\n1.先把牛肉及骨头用清水洗净，然后在水里浸泡四小时(血水留下另用)，将牛肉及骨头下入温水锅，等即将要开时撇去浮沫，加入盐4两，草果5钱，姜皮5钱及花椒2钱用纱布包成调料包清水淘洗去尘后，也放入锅里，小火炖五小时即熟，捞出稍凉后切成1厘米见方的丁。\n\n2.牛肝切小块放入另一锅里煮熟后澄清备用。\n桂籽、花椒、草果、姜皮温火炒烘干碾成粉末，萝卜洗净切成片煮牛肉面熟。\n蒜苗、葱花切未、香莱切小节待用。\n\n3.将肉汤撇去浮油，把泡肉的血水倒入煮开的肉汤锅里，待开后撇沫澄清，加入各种调料粉，再将清澄的牛肝汤倒入水少许，烧开除沫，再加入盐、胡椒粉、味精、熟萝卜片和撇出的浮油。\n\n4.面粉30斤加水18斤擀成面索，再揉和均匀，用灰水7两(如果灰水浓则少加，灰水淡则多加)搅和揉匀。\n案子上擦抹清油，将面搓成条，揪为重五两的条，上面盖上湿条布，然后根据每个人善食的爱好，分别拉成大宽、韭叶、二细、芥麦棱(三角条)一窝丝等形状的面条下锅，面熟后捞入碗内，将牛肉汤、萝卜、肉丁、浮油适量，浇在面条上即成。\n并以每个人的口味加上适量的香莱、蒜苗、葱花及辣子油。\n
